void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int i;
	int arg;

	arg = 1;
	while (arg < argc && argc < 32767)
	{
		i = 0;
		while (argv[arg][i] != '\0')
		{
			ft_putchar(argv[arg][i]);
			i++;
		}
		ft_putchar('\n');
		arg++;
	}
	return (0);
}
