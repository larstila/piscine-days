char	ft_str_is_numeric(char *str)
{
	int i;
	int ret;

	ret = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(48 <= str[i] && str[i] <= 57))
		{
			ret = 0;
			break ;
		}
		i++;
	}
	return (ret);
}
