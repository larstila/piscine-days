#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
	return ;
}


void	ft_print_reverse_alphabet(void)
{
	char c;

	c = 'z';
	while (c >= 'a')
	{
		ft_putchar(c);
		c--;
	}
	ft_putchar('\n');
	return ;
}

int		main (void)
{
	ft_print_reverse_alphabet();
	return (0);
}
